#include "sort.h"


/*
 * - insertionSort将一个序列分成前后两个区域，前面的区域已经排好序
 * - 后面的区域未排序，每次在未排序区域选一个数出来，插入到排好序的区域中
 * - 保证插入后，前面的区域仍然是有序的，这样在未排序区域为空时，排序完成。
 **/

void insertionSort(int a[],int n) {
	for (int i = 1; i < n; ++i) {		//只有一个元素时，显然有序，故迭代从1开始。
		int key = a[i];					//插入数据直接选取未排序序列的第一个
		int k;
		//在排好序的序列中找出插入位置
		for (k = i - 1; k >= 0 && a[k] > key; k--)
			a[k + 1] = a[k];			
		a[k + 1] = key;
	}
}
